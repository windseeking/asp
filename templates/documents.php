              <h4 id="item-a-2">2. LEGAL STATUS OF THE ASSOCIATION</h4>
              <h5 id="item-2-1">2.1</h5>
              <p>Association is a business association of companies initiated on a voluntary basis by companies-
                founders in
                accordance with Art. 120 of the Commercial Code of Ukraine.</p>
              <h5 id="item-2-2">2.2</h5>
              <p>Association has been established and operates under the Memorandum of Association and these Articles of
                Association.</p>
              <h5 id="item-2-3">2.3</h5>
              <p>The Association is a legal entity with its own balance sheet, current and other bank accounts, a round
                seal
                with its name, logo, letterhead and other necessary attributes. Association owns, uses and disposes of
                own
                property according to the laws of Ukraine and these Articles of Association.</p>
              <h5 id="item-2-4">2.4</h5>
              <p>The Association is a nonprofit organization funded by contributions from its members and other sources
                not
                prohibited by law.</p>
              <h5 id="item-2-5">2.5</h5>
              <p>The Association complies with requirements of antitrust law. The establishment of the Association does
                not
                enhance consistency across the markets of its members, nor does it restrict or distort competition
                between
                them. The Association is not entitled to interfere in the economic activities of its members.</p>
              <h5 id="item-2-6">2.6</h5>
              <p>The Association has the right to act as a plaintiff, defendant, and third party in courts. The state,
                its
                bodies and agencies, members of the Association are not responsible for the obligations of the
                Association, as
                well as the Association is not responsible for the obligations of the state, its bodies and agencies,
                members
                of the Association.</p>
              <h5 id="item-2-7">2.7</h5>
              <p>The Association is responsible for its obligations with own property which may be seized under the law
                of
                Ukraine. Its members do not retain rights to the property transferred to the ownership of the
                Association.</p>
              <h5 id="item-2-8">2.8</h5>
              <p>The Association has the right to establish branches, representative offices and other separate units
                both in
                Ukraine and abroad, provided that their activities meet the statutory objectives of the Association, in
                the
                manner prescribed by current legislation of Ukraine, laws and regulations of relevant states and
                international
                treaties. Branches, representative offices and other separate subdivisions of the Association shall be
                unincorporated and shall act on the basis of regulations as approved by the General Assembly.</p>
              <h5 id="item-2-9">2.9</h5>
              <p>Members of the Association shall maintain their independence and legal personality.</p>
              <h5 id="item-2-10">2.10</h5>
              <p>The Association is entitled to:</p>
              <h6>2.10.1</h6>
              <p>enter into any transactions not prohibited by law, including with foreign partners;</p>
              <h6>2.10.2</h6>
              <p>set at its discretion forms, systems, amounts of remuneration for employees and establish special trust
                funds
                in accordance with the law and these Articles of Association;</p>
              <h6>2.10.3</h6>
              <p>represent the interests of its members on the basis of a General Assembly resolution;</p>
              <h6>2.10.4</h6>
              <p>engage Ukrainian and foreign specialists on the basis of any contracts / agreements;</p>
              <h6>2.10.5</h6>
              <p>transfer material and other monetary values to other businesses, organizations and individuals which
                provide
                services to the Association;</p>
              <h6>2.10.6</h6>
              <p>receive bank credits.</p>

