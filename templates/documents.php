              <h4 id="item-a-6">6. ASSOCIATION MANAGEMENT</h4>

              <h5>6.1</h5>
              <p>Management in general</p>
              <h6>6.1.1</h6>
              <p>Association management consists of the general assembly of the Association members (throughout the
                Articles of Association “General Assembly”), the Board and the executive director of the
                Association.</p>
              <h6>6.1.2</h6>
              <p>General Assembly or the Board may create specialized working bodies (commissions, committees, working
                groups etc.) and elect the president and a honorary president of the Association.</p>
              <h6>6.1.3</h6>
              <p>To control the executive bodies of the Association an audit committee may be formed.</p>
              <h6>6.1.4</h6>
              <p>Representatives of an Association member may not hold positions in the Board and in the audit committee
                simultaneously.</p>

              <h5>6.2</h5>
              <p>General Assembly of the Association</p>
              <h6>6.2.1</h6>
              <p>General Assembly is the supreme governing body of the Association, which is competent to decide on any
                issue, including those within the competence of other bodies.</p>
              <h6>6.2.2</h6>
              <p>The exclusive competence of the General Assembly shall be:</p>
              <ol style="list-style-type: lower-alpha;">
                <li>adoption of the Articles of Association, as well as amendments thereto;</li>
                <li>amendments to the Memorandum of Association;</li>
                <li>election of the Board, the executive director and the audit committee;</li>
                <li>considering annual reports of the Board, audit committee and making decisions as appropriate;</li>
                <li>approving an annual budget of the Association;</li>
                <li>disposition of property of the Association in an amount exceeding 50 % of its assets;</li>
                <li>initiating random audits of the Association management;</li>
                <li>establishing of branches, representative offices and other units of the Association;</li>
                <li>making decisions regarding membership in the Association;</li>
                <li>delegation of its powers to the Board of the Association;</li>
                <li>termination of the Association or spin-offs, appointment of a termination committee (liquidation
                  commission, commission on reorganization), defining its powers, approval of the report the termination
                  committee.
                </li>
              </ol>
              <p>The General Assembly of the Association may decide on other matters of the Association.</p>
              <h6>6.2.3</h6>
              <p>General Assembly may delegate some powers referred herein to their competence, to the Board.</p>
              <h6>6.2.4</h6>
              <p>Annual General Assembly shall address the annual report of the Board and approve the annual budget of
                the Association. Annual General Assembly of the Association shall be convened by the Board annually. In
                all other cases General Assembly shall be extraordinary.</p>
              <h6>6.2.5</h6>
              <p>The Board of the Association may decide to convene the General Assembly on its own initiative or upon
                submission of the executive director, audit committee of the Association, or at the request of not less
                than 10 % of Association members. General Assembly shall be held by exchange of emails between members
                and the Association management on email addresses posted on the website of the Association.</p>
              <h6>6.2.6</h6>
              <p>If the General Assembly is convened by the Board of the Association, it approves the agenda. The
                agenda, draft resolutions on each agenda item and other necessary materials shall be sent by the
                executive director of the Association on email addresses of Association members listed in the online
                membership catalog of the Association. 10 % of Association members are entitled to convene the General
                Assembly by sending e-mails to all members of the Association including materials listed in this
                subparagraph.</p>
              <h6>6.2.7</h6>
              <p>Within 10 calendar days after receiving e-mails members of the Association must e-mail to all members
                of the Association their opinions and suggestions on the agenda of the General Assembly. If a member
                suggests inclusion of a new item to the agenda, he sends a draft resolution on this item. Members may
                also send alternative draft resolutions on the agenda items.</p>
              <h6>6.2.8</h6>
              <p>All suggestions received within the above period shall be processed by the executive director (or 10 %
                of Association members), who then e-mails to members for voting updated agenda with draft resolutions on
                each agenda item. Members must vote on each draft resolution within 5 calendar days after receipt of
                e-mail. Members shall vote by e-mailing to all members of the Association of their votes on each draft
                resolution regarding each agenda item. Each member of the Association is entitled to cast one vote.</p>
              <h6>6.2.9</h6>
              <p>The first business day following those 5 calendar days the executive director of the Association (or 10
                % of Association members) shall process the responses received and determine a quorum. There is a quorum
                if responses are received from at least 2/3 members of the Association. In the absence of a quorum
                General Assembly shall be deemed to have failed.</p>
              <h6>6.2.10</h6>
              <p>If there is a quorum, a resolution of the General Assembly is adopted, provided that simple majority of
                Association members which responded voted in favor of it. A member is not entitled to vote at the
                General Assembly regarding a transaction or a dispute between him and the Association.</p>
              <h6>6.2.11</h6>
              <p>A resolution regarding amendments to the Articles of Association, alienation of the property of
                Association worth 50 % or more of its assets, as well as the termination of the Association is adopted
                if no less than 3/4 Association members participating in the General Assembly voted in favor of it.</p>
              <h6>6.2.12</h6>
              <p>Resolutions of the General Assembly shall be included in its minutes signed by the Chairman of the
                Board and the executive director of the Association, or 10 % of Association members, and e-mailed to all
                members of the Association.</p>
              <h6>6.2.13</h6>
              <p>Resolutions of the General Assembly are binding on members of the Association and the Board.</p>
              <h6>6.2.14</h6>
              <p>If a member of the Association believes that the voting procedure at the General Assembly was violated,
                he may declare it to the Board, which considers the complaint. In particular this applies to cases of
                unauthorized e-mailing on behalf of a member. In such cases, the member may withdraw his vote within 10
                calendar days after receiving e-mail with copies of minutes of the General Assembly. The member who
                withdraws his vote shall notify all members of the Association of this. Within 3 business days after
                receiving withdrawal e-mail executive director of the Association (or 10 % of Association members)
                e-mails to all members of the Association either corrected minutes, or notification of the General
                Assembly failure.</p>

              <h5>6.3</h5>
              <p>The Board of the Association</p>
              <h6>6.3.1</h6>
              <p>The Board is a permanent collegial body of the Association. The Board will coordinate the activities of
                the Association between General Assemblies.</p>
              <h6>6.3.2</h6>
              <p>The initial composition of the Board is determined by the Memorandum of Association. The term of
                service of the Board shall be until its re-election by the General Assembly. The Board may be re-elected
                by the General Assembly of the Association by a simple majority vote for indefinite term and shall
                consist of authorized representatives of companies members of the Association in an amount determined by
                the General Assembly.</p>
              <h6>6.3.3</h6>
              <p>The minimum composition of the Board shall be two members: the chairman and the secretary of the
                Board.</p>
              <h6>6.3.4</h6>
              <p>Board meetings shall be convened by the chairman of the Board where necessary. The Board shall be
                competent if its session involve at least 2/3 members. Resolutions are passed by simple majority of
                members present at the meeting.</p>
              <h6>6.3.5</h6>
              <p>Each member of the Board is entitled to cast one vote. In case of equal votes, the voice of the
                chairman of the Board shall be decisive.</p>
              <h6>6.3.6</h6>
              <p>Board’s resolutions shall take form of minutes of the Board’s meetings to be signed by the chairman and
                the secretary of the Board.</p>
              <h6>6.3.7</h6>
              <p>Board’s resolutions shall be binding upon Board’s members and members of the Association, provided they
                are in line with resolutions of the General Assembly and legislation of Ukraine.</p>
              <h6>6.3.8</h6>
              <p>The Board:</p>
              <ol style="list-style-type: lower-alpha;">
                <li>approves the plans and estimates of the Association, submits to the General Assembly draft budget;
                </li>
                <li>establishes the amount, payment procedures for initial, membership fees and targeted
                  contributions;
                </li>
                <li>reports to the General Assembly;</li>
                <li>establishes the application form for accession to the Memorandum of Association and questionnaires
                  for joining new members to the Association;
                </li>
                <li>disposes of property of the Association, including funds;</li>
                <li>decides to commit transactions on behalf of the Association;</li>
                <li>controls the executive director of the Association;</li>
                <li>approves the staffing of the Association, makes decisions to hire or to dismiss employees of the
                  Association, pay remuneration to them;
                </li>
                <li>considers other matters of the Association, except those belonging to the exclusive competence of
                  the General Assembly.
                </li>
              </ol>
              <h6>6.3.9</h6>
              <p>The powers of the Board’s members may be early terminated:</p>
              <ol style="list-style-type: lower-alpha;">
                <li>voluntary at their own discretion;</li>
                <li>by the resolution of the General Assembly.</li>
              </ol>
              
              <h5>6.4</h5>
              <p>The executive director of the Association</p>
              <h6>6.4.1</h6>
              <p>The executive director of the Association is elected by the General Assembly for a period until the
                next annual General Assembly of the Association. In the absence or inability to perform his duties, the
                functions of the executive director shall be performed by the chairman of the Board. The executive
                director may resign by submitting to the Board a statement of resignation.</p>
              <h6>6.4.2</h6>
              <p>The executive director of the Association:</p>
              <p>is a manager of the Association, representing it before governmental agencies, international
                organizations, other legal entities and individuals;
              </p>
              <p>acts on behalf of the Association without a proxy, is in charge of general management in accordance
                with the laws of Ukraine and these Articles of Association;
              </p>
              <p>opens bank accounts, signs banking and financial instruments, controls costs and cash flow in the
                accounts of the Association;
              </p>
              <p>represents the Association in international organizations and public authorities;</p>
              <p>controls the preparation of proposals and documents concerning the Association;</p>
              <p>coordinates activities of working bodies of the Association;</p>
              <p>represents the Association in relations with Ukrainian and foreign natural and legal persons in all
                matters of the Association within the competence and in line with resolutions of the General Assembly
                and the Board;
              </p>
              <p>ensures implementation of resolutions adopted by the General Assembly and the Board;</p>
              <p>makes decisions about starting and canceling litigations and settlement of claims on behalf of the
                Association in Ukraine and abroad;
              </p>
              <p>enters on behalf of the Association into contracts, agreements and treaties, commits transactions as
                resolved by the Board;
              </p>
              <p>issues powers of attorney (including the right of substitution) on behalf of the Association;</p>
              <p>submits documents for state registration of the Association and registration with tax and other
                authorities, opens current and other accounts of the Association;
              </p>
              <p>disposes of property and funds of the Association on the basis of Board’s resolutions;</p>
              <p>on the basis of Board’s resolutions hires and dismisses employees of the Association, as well as
                managers of branches, representative offices and other units under the laws of Ukraine, takes measures
                to encourage employees and to punish them;
              </p>
              <p>issues orders and instructions in matters of the Association;</p>
              <p>reports to the Board.</p>

