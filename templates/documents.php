                with companies in Finland, Finnish or other foreign legal entity having or willing to establish contacts
                with Ukrainian companies, which agrees to perform obligations and enjoy rights under the constitutional
                documents of the Association. Membership in the Association shall be discrimination free.</p>

              <h5 id="item-5-3">5.3</h5>
              <p>Association members are its founders as well as legal entities who joined the Association after its
                state registration.</p>

              <h5 id="item-5-4">5.4</h5>
              <p>Founders of the Association and entities which joined the Association after its state registration
                shall have equal rights and duties. The provisions of these Articles of Association apply equally to
                both founders of the Association and other members regarding their rights and obligations hereunder.</p>

              <h5 id="item-5-5">5.5</h5>
              <p>A company becomes a member of the Association after appropriate resolution of the General Assembly of
                the Association.</p>

              <h5 id="item-5-6">5.6</h5>
              <p>The founders of the Association are its members since the state registration of the Association.</p>

              <h5 id="item-5-7">5.7</h5>
