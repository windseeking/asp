              <h4 id="item-a-4">4. PROPERTY AND FUNDS OF ASSOCIATION</h4>

              <h5 id="item-4-1">4.1</h5>
              <p>The Association may own or enjoy other rights on real estate, funds and other assets necessary to
                perform its statutory activities.</p>

              <h5 id="item-4-2">4.2</h5>
              <p>Property of the Association consists of fixed assets and working capital and other assets created or
                acquired by the Association the value of which is shown on its balance sheet.</p>

              <h5 id="item-4-3">4.3</h5>
              <p>Funds of the Association shall be formed from membership fees: initial, annual and targeted
                contributions of the Association (whether in a tangible form or not), as well as passive income and
                arbitration fees.</p>

              <h5 id="item-4-4">4.4</h5>
              <p> Membership fees and contributions may take the form of money (including foreign currency), securities,
                other assets, property and moral rights. The value of a contribution in the form of property shall be
                agreed between the member and the General Assembly of the Association in Ukrainian hryvnias. Association
                shall have the exclusive right to dispose of and use of contributed property.</p>

              <h5 id="item-4-5">4.5</h5>
              <p>The amount of initial and annual membership fees as well as the payment procedure shall be established
                by the Board of the Association.</p>

              <h5 id="item-4-6">4.6</h5>
              <p>The Board of the Association shall manage property of Association in line with the law and these
                Articles of Association.</p>

              <h5 id="item-4-7">4.7</h5>
              <p>The Association is the owner of the property legally separated from the assets of founders and members
                of the Association.</p>

              <h5 id="item-4-8">4.8</h5>
              <p>Money or property received by the Association shall be spent only on activities listed in the
                constitutional documents, staff, branches and representative offices and are not subject to distribution
                among its members in case of winding up, nor can be used to benefit any individual member of the
                Association or its officials (except for payment of their work and social security contributions).</p>

              <h5 id="item-4-9">4.9</h5>
              <p>Financial activities of the Association are based on a budget approved by the General Assembly. Excess
                costs or savings on certain budget items shall be discussed by the Board to find appropriate solution
                with subsequent consideration and approval of the General Assembly of the Association.</p>

