              <h4 id="item-b-1">1. SUBJECT OF THE MEMORANDUM</h4>
              <h5 id="item-1-1">1.1</h5>
              <p>The Parties on a voluntary basis establish association of businesses - Suomi Partnership Association
                (hereinafter referred to as the "Association") to continually coordinate economic activity without
                interference
                in production and commercial activities of any of the Parties.</p>

              <h5 id="item-1-2">1.2</h5>
              <p>The full name of the Association:<br>
                in Ukrainian — АСОЦІАЦІЯ «СУОМІ ПАРТНЕРШИП»;<br>
                in English — ASSOCIATION «SUOMI PARTNERSHIP».</p>

              <h5 id="item-1-3">1.3</h5>
              <p>The abbreviated name of the Association:<br>
                in Ukrainian — АСП;<br>
                in English — ASP.</p>

              <h5 id="item-1-4">1.4</h5>
              <p>Association has been established for an indefinite period as a non-profit
                organization whose mission is to enhance cooperation between Ukrainian and Finnish companies.</p>

              <h5 id="item-1-5">1.5</h5>
              <p>This Memorandum defines the obligations and procedure for joint establishment of the Association,
                including
                the terms of contributions.</p>

              <h4 id="item-b-2">2. THE ARTICLES OF ASSOCIATION</h4>
              <h5 id="item-2-1">2.1</h5>
              <p>The Association will operate under this Memorandum and Articles of Association. Covenants of the
                Parties
                herein
                shall be considered a resolution of the supreme body of the Association. After the state registration of
                the
                Association
                such resolutions should be made in the manner prescribed by Articles of Association. The terms of this
                Memorandum will have
                precedence over the Articles of Association until appropriate resolutions are passed by the supreme body
                of
                the
                Association as prescribed by the Articles of Association.</p>

              <h5 id="item-2-2">2.2</h5>
              <p>The Parties hereby approve the Articles of Association.</p>

              <h5 id="item-2-3">2.3</h5>
              <p>Olexandr Bondar is hereby empowered to register the Association on behalf of the Parties
                with rights to sign, submit and receive documents on behalf of the Parties.</p>

              <h4 id="item-b-3">3. ASSOCIATION MEMBERSHIP</h4>
              <h5 id="item-3-1">3.1</h5>
              <p>After the state registration of the Association Parties receive the status of the Association members.
                Further admission to the Association will be in accordance with the Articles of Association.</p>
              <h5 id="item-3-2">3.2</h5>
              <p>Admission to the Association provides for adherence to the Memorandum of Association. Candidates for
                members
                of the Association shall submit an application to join this Memorandum. Should the membership of the
                applicant
                be approved by the supreme body of the Association, this application and resolution of the supreme body
                of the
                membership approval become an integral part hereof.</p>
              <h5 id="item-3-3">3.3</h5>
              <p>Membership cancellation means cancellation of participation in this Memorandum. Application for the
                membership cancellation and the resolution of the supreme body of the Association in this respect shall
                be
                an integral part hereof.</p>

              <h4 id="item-b-4">4. CONTRIBUTIONS OF THE PARTIES</h4>
              <h5 id="item-4-1">4.1</h5>
              <p>To establish the Association the founders shall make contributions.</p>

              <h5 id="item-4-2">4.2</h5>
              <p>Contributions shall be made by the founders within one month from the date of state registration of the
                Association.</p>

              <h5 id="item-4-3">4.3</h5>
              <p>Costs for the state registration of the Association shall bear the first founder.</p>

              <h5 id="item-4-4">4.4</h5>
              <p>Further initial, annual and targeted contributions shall be determined by the Board of the Association
                in accordance with the Articles of Association.</p>

              <h4 id="item-b-5">5. LOCATION OF THE ASSOCIATION</h4>
              <h5 id="item-5-1">5.1</h5>
              <p>The Association shall be located in the premises of the first founder.</p>

              <h5 id="item-5-2">5.2</h5>
              <p>The location of the Association shall be: Ukraine, Odesa, Uspens’ka str., 26, of. 10.</p>

              <h4 id="item-b-6">6. ELECTION OF THE BODIES OF THE ASSOCIATION</h4>
              <h5 id="item-6-1">6.1</h5>
              <p>The Board of the Association shall consist of 2 persons:<br>
                а) Olexandr Sakara – the chairman of the Board;<br>
                б) Andriy Sakara – the secretary of the Board</p>

              <h5 id="item-b-6-2">6.2</h5>
              <p>The Board will operate for an indefinite period, until its re-election by the supreme body of the
                Association
                in accordance with the Articles of Association. The chairman and the secretary of the Board shall
                perform their
                duties free of charge.</p>

              <h5 id="item-6-3">6.3</h5>
              <p>Ladymyr Bondar is elected as the executive director of the Association until the date of the next
                annual
                general assembly of the members of the Association. The executive director shall perform his duties free
                of charge.</p>

              <h4 id="item-b-7">7. RESPONSIBILITY OF THE PARTIES</h4>
              <h5 id="item-7-1">7.1</h5>
              <p>For violation hereof a Party shall be liable under the Articles of Association and laws of Ukraine.</p>

              <h5 id="item-7-2">7.2</h5>
              <p>A defaulting Party undertakes to fully compensate the damage made to the other Party by its failure or
                improper performance of the duties hereunder.</p>

              <h4 id="item-b-8">8. AMENDMENTS AND TERMINATION HEREOF</h4>
              <h5 id="item-8-1">8.1</h5>
              <p>Until the moment of state registration of the Association Parties may amend this Memorandum, and may
                terminate
                it by mutual written agreement.</p>

              <h5 id="item-8-2">8.2</h5>
              <p>After the state registration of the Association amendments hereto, and decision to terminate the
                Association
                shall be passed by the supreme body of the Association.</p>

              <h4 id="item-b-9">9. MISCELLANEOUS</h4>
              <h5 id="item-9-1">9.1</h5>
              <p> In all matters not covered herewith the Parties shall be governed by the Articles of Association and
                Ukrainian laws.</p>

              <h5 id="item-9-2">9.2</h5>
              <p>The Association will assume responsibility for compliance with antitrust laws. The Parties are
                responsible
                for compliance with the Model requirements for establishment of a business association in view of total
                exemption from obtaining the permission of the Antimonopoly Committee of Ukraine.</p>

              <h5 id="item-9-3">9.3</h5>
              <p>This memorandum is made in three originals, one for each of the founders and to the state
                registrar.</p>

              <h5 id="item-9-4">9.4</h5>
              <p>This Agreement shall enter into force upon its signature by the founders.</p>
