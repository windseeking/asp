              <h4 id="item-a-7">7. THE AUDIT COMMITTEE OF THE ASSOCIATION</h4>

              <h5>7.1</h5>
              <p>To control the administrative, economic and financial activities of the Board and the executive
                director of the Association and its branches, representative offices and other units, the General
                Assembly may elect an audit committee. The composition and terms of office of the audit committee shall
                be determined in the resolution of the General Assembly on its election.</p>

              <h5>7.2</h5>
              <p>In case the audit committee is formed, it shall annually report to the General Assembly on its
                activities.</p>

              <h5>7.3</h5>
              <p>The audit committee shall:</p>
              <h6>7.3.1</h6>
              <p>audit the implementation of resolutions of the General Assembly and the Board;</p>
              <h6>7.3.2</h6>
              <p>check the annual balance sheet of the Association and other financial and accounting records;</p>
              <h6>7.3.3</h6>
              <p>notify the General Assembly of all violations discovered during an inspection.</p>

              <h5>7.4</h5>
              <p>Association member may not have representatives in the audit committee and management of association
                simultaneously.</p>

              <!--        8        -->
              
              <h4 id="item-a-8">8. ACCOUNTING AND REPORTING IN THE ASSOCIATION</h4>

              <h5>8.1</h5>
              <p>The Association shall keep records, accounts, tax and statistical accounting and reporting in line with
                the legislation of Ukraine.</p>

              <h5>8.2</h5>
              <p>The financial year of the Association shall be from January 1 to December 31 of the calendar year,
                inclusive.</p>

              <h5>8.3</h5>
              <p>Annual report of the Association is subject to approval by the General Assembly of the Association.</p>

              <h5>8.4</h5>
              <p>The Association shall submit reports to the tax and other public authorities of Ukraine entitled to
                control business entities.</p>

              <!--        9        -->
              
              <h4 id="item-a-9">9. TERMINATION OF THE ASSOCIATION</h4>

              <h5>9.1</h5>
              <p>Association may be terminated by either reorganization, or dissolution.</p>

              <h5>9.2</h5>
              <p>Association may be dissolved by resolution of the General Assembly of the Association or by a
                court.</p>

              <h5>9.3</h5>
              <p>In case of dissolution of the Association:</p>
              <h6>9.3.1</h6>
              <p>dissolution of the Association shall be reported via the media and by email to every member of the
                Association;</p>
              <h6>9.3.2</h6>
              <p>General Assembly shall appoint the liquidation commission in charge of preparation of the liquidation
                balance sheet, fulfilling obligations under agreements in force and entering into new agreements within
                its competence, preparation of the list of creditors and debtors of the Association, disposing of the
                remaining assets. The liquidation commission acts, enjoys rights and bears obligations and
                responsibilities under the legislation of Ukraine;</p>
              <h6>9.3.3</h6>
              <p>Property and other assets of the Association, remaining after payment of taxes to the budget, payments
                to creditors and employees, shall be transferred to another nonprofit organization of similar type or to
                the state budget.</p>
              <h6>9.3.4</h6>
              <p>Association is terminated when removed from the Unified State Register of Legal Entities and Natural
                Persons – Entrepreneurs.</p>

              <h5>9.4</h5>
              <p>The Association may be reorganized by a resolution of the General Assembly of the Association. In case
                of reorganization all rights and duties of the Association shall be transferred to a successor.</p>

              <h5>9.5</h5>
              <p>Spin-offs may take place out of the Association by a resolution of the General Assembly in accordance
                with the law of Ukraine.</p>
