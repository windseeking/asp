              <h4 id="item-a-3">3. ACTIVITIES OF THE ASSOCIATION</h4>

              <h5 id="item-3-1">3.1</h5>
              <p>Association is aimed at promoting cooperation between Ukrainian and Finnish companies.</p>

              <h5 id="item-3-2">3.2</h5>
              <p>Mission of the Association shall be to coordinate economic activities of its members without
                interference in
                their business activities and without the right to binding decisions regarding their management.</p>

              <h5 id="item-3-3">3.3</h5>
              <p>
                Activities of the Association shall be limited to:</p>
              <p>promoting objectives of the Association by organizing lectures, discussions, seminars and conferences,
                speeches in the media, consulting, engaging local and foreign experts and consultants;</p>
              <p>dissemination of scientific and technical knowledge, best practices of efficient technologies,
                introducing measures to reduce costs, environmentally friendly technologies;</p>
              <p>providing a training system for its members;</p>
              <p>regularly publishing information about the Association and its members, industry issues, foreign
                experience;</p>
              <p>organization of analytical work on management and marketing in the relevant markets and regular
                dissemination of their results among members of the Association;</p>
              <p>providing consultations to members of the Association to improve work or train their employees;</p>
              <p>gathering from members of the Association of the following data: production and production capacity,
                problems arising in the course of their activities in order to find best ways to solve them, information
                that may contribute to the establishment and development of relations of cooperation with authorities
                and other organizations, technical information and education of Association members (collected
                information shall be distributed in a summary form after a month from its collection and can not be used
                against members of the Association, competitors or customers, and shall not include data on essential
                conditions of economic activity, prices, business strategies, exchange of which may facilitate the
                coordination of competitive behavior and the possibility to predict the behavior of other members of the
                Association, competitors and consumers);</p>
              <p>coordination of members towards harmonization of Ukrainian and Finnish standards;</p>
              <p>assistance in creating conditions for penetration of the Association members to overseas markets in
                ways
                that do not restrict competition;</p>
              <p>protection and representation of common lawful interests and collective opinions of members of the
                Association towards legislative and executive bodies in relationships with governmental agencies,
                international and non-governmental organizations of Ukraine and Finland;</p>
              <p>making proposals to governmental bodies and agencies on matters relating to cooperation of Ukrainian
                and
                Finnish companies;</p>
              <p>arranging independent examinations of legislative and regulatory legal acts, as well as their drafts to
                promote co-operation of Ukrainian and Finnish companies;</p>
              <p>arranging scientific research on economical, legal, technological and other issues of cooperation of
                Ukrainian and Finnish companies;</p>
              <p>projects that promote enhancement and development of cooperation of Ukrainian and Finnish
                companies;</p>
              under the auspices of the Association a permanent court of arbitration shall be established according to
              the law.</p>

