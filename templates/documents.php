                authorized representatives (individuals) according to these Articles of Association.</p>

              <h5 id="item-5-8">5.8</h5>
              <p>Relationships between the Association and its members, their mutual rights and obligations are governed
                by these Articles of Association.</p>

              <h5 id="item-5-9">5.9</h5>

              <p>Applications and complaints of the Association members shall be considered by the Board of the
                Association.</p>
