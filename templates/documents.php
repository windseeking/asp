              <h4 id="item-a-5">5. MEMBERS OF THE ASSOCIATION</h4>

              <h5 id="item-5-1">5.1</h5>
              <p>New members are free to join the Association, and actual members are free to cancel their
                membership.</p>

              <h5 id="item-5-2">5.2</h5>
